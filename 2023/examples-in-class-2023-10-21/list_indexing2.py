list1 = ["zero","one","two","three","four","five"]

# we can use range like structure to index the list
# instead of one element, we get sublist

sublist1 = list1[0:4]
print(sublist1)
print("another example")
sublist2 = list1[2:4]
print(sublist2)

print("start end step")
sublist3 = list1[0:3:2]
print(sublist3)
# 0,2
