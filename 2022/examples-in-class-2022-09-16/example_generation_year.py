print("please enter the year you have born")
string_input = input()
born_year = int(string_input)

if born_year >= 1965 and born_year <= 1980:
	print("You are generation: Gen X")
if born_year >= 1981  and born_year <= 1996:
	print("You are generation: Millennials")


